@dataclass
class ProjectMeta:
    name: str
    main_repo: str
    language: str
    homepage: str
    primary_contact: str

    # Some OSS-Fuzz projects don't carry `main_repo` in project.yaml
    # because their build.sh fetches the source via a custom URL. Hardcode
    # the canonical repo URL for these so the orchestrator can clone.
    REPO_FALLBACKS: ClassVar[dict[str, str]] = {
        "bzip2": "https://gitlab.com/bzip2/bzip2.git",
    }

    @classmethod
    def fetch(cls, name: str) -> "ProjectMeta":
        url = PROJECT_YAML_URL.format(name=name)
        with urllib.request.urlopen(url, timeout=20) as resp:
            body = resp.read().decode("utf-8")
        meta = {"language": "", "homepage": "", "primary_contact": "", "main_repo": ""}
        for line in body.splitlines():
            line = line.strip()
            for key in meta:
                prefix = f"{key}:"
                if line.startswith(prefix):
                    value = line[len(prefix):].strip().strip("'\"")
                    meta[key] = value
        if not meta["main_repo"]:
            fallback = cls.REPO_FALLBACKS.get(name, "")
            if fallback:
                meta["main_repo"] = fallback
            else:
                raise SystemExit(f"OSS-Fuzz {name}: project.yaml is missing main_repo")
        return cls(name=name, **meta)


def _run(cmd: list[str], *, cwd: Optional[Path] = None, check: bool = True,
         env: Optional[dict] = None, capture: bool = False) -> subprocess.CompletedProcess:
    """Wrapper around subprocess.run with sane defaults."""
    return subprocess.run(
        cmd,
        cwd=str(cwd) if cwd else None,
        check=check,
        env=env,
        stdout=subprocess.PIPE if capture else None,
        stderr=subprocess.PIPE if capture else None,
        text=True,
    )


def clone_or_pull(meta: ProjectMeta, corpus_root: Path) -> Path:
    """Clone main_repo (shallow) or fast-forward an existing checkout.

    Returns the path to the working tree.
    """
    target = corpus_root / meta.name
    if target.exists() and (target / ".git").exists():
        try:
            _run(["git", "fetch", "--depth=1", "origin"], cwd=target)
            _run(["git", "reset", "--hard", "FETCH_HEAD"], cwd=target)
        except subprocess.CalledProcessError:
            # If fast-forward fails, fall back to a fresh clone next run.
            pass
        return target
    corpus_root.mkdir(parents=True, exist_ok=True)
    _run(["git", "clone", "--depth=1", meta.main_repo, str(target)])
    return target


def prepare_corpus(project_name: str, project_root: Path) -> None:
    """Run per-project build-prep steps required for `cc -E` to succeed.

    Many OSS-Fuzz C projects rely on autogenerated config headers that
    would normally be produced by ``./configure`` or CMake. For each
    starter-rotation project we ship the shortest-path equivalent here
    so the BMC-Agent pipeline can preprocess the sources without
    invoking the full build system.
    """
    if project_name == "libpng":
        # libpng requires pnglibconf.h (normally generated by ./configure
        # or copied from scripts/pnglibconf/* via the Makefile). It also
        # ships a generic prebuilt config in the repo root; use that.
        # We also disable PNG_TARGET_SPECIFIC_CODE_SUPPORTED so pngtarget.h
        # doesn't chase arm/intel/mips/powerpc subdirs that aren't in our
        # corpus tree — those gate platform-specific SIMD which BMC won't
        # exercise anyway.
        prebuilt = project_root / "pnglibconf.h.prebuilt"
        target = project_root / "pnglibconf.h"
        if prebuilt.exists() and not target.exists():
            text = prebuilt.read_text()
            text = text.replace(
                "#define PNG_TARGET_SPECIFIC_CODE_SUPPORTED",
                "/* #undef PNG_TARGET_SPECIFIC_CODE_SUPPORTED  (bmc-agent corpus) */",
            )
            target.write_text(text)
            print(f"[oss_fuzz_sweep] libpng: synthesized pnglibconf.h (no SIMD)")
        # Nuke the test / utility / contrib subdirs so verify-dir's
        # source discovery cannot pick them up. These contain test
        # programs (readpng2.c, etc.), CLI utilities (pngfix, pngcp),
        # and arch-specific shim files that explode the symbol space.
        # We also remove standalone test/utility .c files at the root.
        import shutil
        for noise in [
            "contrib", "tests",
            # Architecture-specific SIMD subdirs — bmc-agent's source
            # discovery walks into them; each has its own .c files
            # implementing the per-arch hooks. CBMC verifies them safe
            # trivially, no value to sweep.
            "arm", "intel", "mips", "powerpc", "loongarch", "riscv",
            # Build / metadata subdirs that may also contain .c.
            "ci", "scripts", "projects", "manuals", "LICENSES",
        ]:
            d = project_root / noise
            if d.exists():
                shutil.rmtree(d, ignore_errors=True)
                print(f"[oss_fuzz_sweep] libpng: removed {noise}/")
        for noise_c in [
            "example.c", "pngtest.c", "makepng.c", "pngsimd.c",
            "pnggetset.c", "pngvalid.c", "pngstest.c", "pngimage.c",
            "pngcp.c", "pngfix.c",
        ]:
            p = project_root / noise_c
            if p.exists():
                p.unlink()
                print(f"[oss_fuzz_sweep] libpng: removed {noise_c}")
    elif project_name == "libtiff":
        # Delete libtiff debug / build utility files that have main() and
        # confuse bmc-agent's harness gen (verified CONVERSION ERROR on
        # mkspans.c during the OpenRouter sweep 2026-05-29).
        for noise_c in ["mkspans.c", "mkg3states.c"]:
            p = project_root / "libtiff" / noise_c
            if p.exists():
                p.unlink()
                print(f"[oss_fuzz_sweep] libtiff: removed libtiff/{noise_c}")
        # libtiff: ship a minimal tif_config.h / tiffconf.h via cmake or
        # copy from the bundled templates. Both live under libtiff/.
        import re
        for tmpl, dest in [
            ("tif_config.h.cmake.in", "tif_config.h"),
            ("tiffconf.h.cmake.in", "tiffconf.h"),
            ("tiffvers.h.cmake.in", "tiffvers.h"),
        ]:
            src = project_root / "libtiff" / tmpl
            dst = project_root / "libtiff" / dest
            if src.exists():
                text = src.read_text()
                # #cmakedefine01 FOO  -> #define FOO 1
                text = re.sub(r"^#cmakedefine01\s+(\S+).*$",
                              r"#define \1 1", text, flags=re.M)
                # #cmakedefine FOO @VAR@  -> /* (left out) */
                text = re.sub(r"^#cmakedefine\s+(\S+)\s+.*$",
                              r"/* #define \1 (omitted) */", text, flags=re.M)
                # #cmakedefine FOO  -> #define FOO 1 for the safe defaults
                for tok in ("HAVE_FCNTL_H", "HAVE_STRINGS_H",
                            "HAVE_SYS_TYPES_H", "HAVE_UNISTD_H",
                            "HAVE_STRING_H", "HAVE_STDLIB_H"):
                    text = re.sub(rf"^#cmakedefine\s+{tok}\s*$",
                                  f"#define {tok} 1", text, flags=re.M)
                # Anything remaining: comment out
                text = re.sub(r"^#cmakedefine\s+",
                              "// #cmakedefine ", text, flags=re.M)
                # @VAR@ substitutions -> safe defaults
                text = text.replace("@TIFF_INT8_FORMAT@", "\"%hhd\"")
                text = text.replace("@TIFF_UINT8_FORMAT@", "\"%hhu\"")
                text = text.replace("@TIFF_INT16_FORMAT@", "\"%hd\"")
                text = text.replace("@TIFF_UINT16_FORMAT@", "\"%hu\"")
                text = text.replace("@TIFF_INT32_FORMAT@", "\"%d\"")
                text = text.replace("@TIFF_UINT32_FORMAT@", "\"%u\"")
                text = text.replace("@TIFF_INT64_FORMAT@", "\"%lld\"")
                text = text.replace("@TIFF_UINT64_FORMAT@", "\"%llu\"")
                # The signed/unsigned size-type and the per-type sizes.
                # On x86_64 Linux these are: ssize_t = signed long, size_t = unsigned long.
                text = text.replace("@TIFF_SIZE_T@", "unsigned long")
                text = text.replace("@TIFF_SSIZE_T@", "signed long")
                text = text.replace("@TIFF_SIZE_FORMAT@", "\"%lu\"")
                text = text.replace("@TIFF_SSIZE_FORMAT@", "\"%ld\"")
                # Size-of types — we run on x86_64 Linux, all 64-bit.
                for sz_key in ("SIZEOF_SIZE_T", "SIZEOF_VOIDP",
                               "SIZEOF_UNSIGNED_LONG", "SIZEOF_LONG"):
                    text = text.replace(f"@{sz_key}@", "8")
                for sz_key in ("SIZEOF_INT", "SIZEOF_UNSIGNED_INT"):
                    text = text.replace(f"@{sz_key}@", "4")
                # Catch-all for any other @VAR@ left
                text = re.sub(r"@[A-Z_]+@", "0", text)
                dst.write_text(text)
                print(f"[oss_fuzz_sweep] libtiff: synthesized {dest}")
    elif project_name == "expat":
        # expat: expat_config.h is autoconf-generated. Use the bundled
        # static defaults from expat/conftools/ if available.
        src = project_root / "expat" / "expat_config.h.in"
        dst = project_root / "expat" / "lib" / "expat_config.h"
        if src.exists() and not dst.exists():
            text = src.read_text()
            # Remove @VAR@ substitutions; we just need the file to exist.
            text = text.replace("/* #undef ", "// #undef ").replace("#undef ", "// #undef ")
            dst.write_text(text)
            print(f"[oss_fuzz_sweep] expat: synthesized expat_config.h")
    elif project_name == "zstd":
        # zstd: headers are inline, no autogen needed for the lib/ tree.
        pass
    elif project_name == "bzip2":
        # bzip2: synthesize bz_version.h from the .in template, delete
        # CLI/test files so verify-dir only sweeps the library code.
        bz_ver_in = project_root / "bz_version.h.in"
        bz_ver = project_root / "bz_version.h"
        if bz_ver_in.exists() and not bz_ver.exists():
            bz_ver.write_text(bz_ver_in.read_text().replace("@BZ_VERSION@", "1.0.8-bmc-agent"))
            print(f"[oss_fuzz_sweep] bzip2: synthesized bz_version.h")
        for noise_c in ["bzip2.c", "bzip2recover.c", "dlltest.c",
                        "mk251.c", "spewG.c", "unzcrash.c"]:
            p = project_root / noise_c
            if p.exists():
                p.unlink()
                print(f"[oss_fuzz_sweep] bzip2: removed {noise_c}")
    elif project_name == "cmark":
        # cmark: CMake generates cmark_version.h (from .in) and
        # cmark_export.h (via generate_export_header). Synthesize both so
        # the src/ tree compiles standalone. No config.h is required by
        # this version (0.31.x dropped it). Drop main.c (CLI entry point).
        src = project_root / "src"
        ver_in = src / "cmark_version.h.in"
        ver = src / "cmark_version.h"
        if ver_in.exists():
            ver.write_text(
                ver_in.read_text()
                .replace("@PROJECT_VERSION_MAJOR@", "0")
                .replace("@PROJECT_VERSION_MINOR@", "31")
                .replace("@PROJECT_VERSION_PATCH@", "2")
            )
            print("[oss_fuzz_sweep] cmark: synthesized cmark_version.h")
        export_h = src / "cmark_export.h"
        if not export_h.exists():
            # Static-analysis build: all visibility/deprecation macros empty.
            export_h.write_text(
                "#ifndef CMARK_EXPORT_H\n#define CMARK_EXPORT_H\n"
                "#define CMARK_EXPORT\n#define CMARK_NO_EXPORT\n"
                "#define CMARK_DEPRECATED\n#define CMARK_DEPRECATED_EXPORT\n"
                "#define CMARK_DEPRECATED_NO_EXPORT\n#define CMARK_NO_DEPRECATED\n"
                "#endif\n"
            )
            print("[oss_fuzz_sweep] cmark: synthesized cmark_export.h")
        main_c = src / "main.c"
        if main_c.exists():
            main_c.unlink()
            print("[oss_fuzz_sweep] cmark: removed src/main.c")
    elif project_name == "brotli":
        # brotli: decoder sources in c/dec are the attack surface. Headers
        # live under c/include/brotli/ and c/common/ (wired via
        # EXTRA_INCLUDE_DIRS). Nothing to synthesize; the encoder (c/enc)
        # and fuzz/ dirs are simply not the swept source_dir.
        pass
    # Else: unknown project — let the smoke test surface what's missing.


