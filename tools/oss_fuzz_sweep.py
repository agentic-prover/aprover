def discover_sources(project_root: Path) -> Path:
    """Pick the directory that holds the .c sources we want to sweep.

    libpng: png*.c sits in the repo root.
    libtiff: libtiff/tif_*.c
    expat: expat/lib/*.c
    zstd: lib/**/*.c (multi-dir; this script's first cut points at lib/)

    Returns the directory bmc-agent should sweep.
    """
    candidates = [
        project_root / "libtiff",                  # libtiff layout
        project_root / "expat" / "lib",            # expat layout
        project_root / "lib" / "decompress",       # zstd: the parser side (attack surface)
        project_root / "lib" / "common",           # zstd common (fallback)
        project_root / "c" / "dec",                # brotli: decoder = attack surface
        project_root / "lib",                      # generic
        project_root / "src",                      # cmark layout
        project_root,                              # libpng layout — last so subdirs win
    ]
    for cand in candidates:
        if cand.is_dir() and any(cand.glob("*.c")):
            return cand
    raise SystemExit(
        f"OSS-Fuzz {project_root.name}: no .c sources discovered in known layouts"
    )


def excludes_for(project_name: str) -> list[str]:
    """Return the source-file glob patterns to skip during verify-dir."""
    return EXCLUDE_PATTERNS.get(project_name, [])


# Extra header search dirs (relative to the *project root*) that the
# auto-include logic in run_verify_dir() can't infer. Needed when public
# headers live in a nested dir (brotli: c/include holds brotli/*.h, so the
# direct-*.h sibling heuristic misses it) or sit beside a shared-code dir.
EXTRA_INCLUDE_DIRS: dict[str, list[str]] = {
    "brotli": ["c/include", "c/common", "c/dec"],
}


def extra_includes_for(project_name: str, project_root: Path) -> list[Path]:
    """Resolve EXTRA_INCLUDE_DIRS entries to existing absolute dirs."""
    out: list[Path] = []
    for rel in EXTRA_INCLUDE_DIRS.get(project_name, []):
        cand = project_root / rel
        if cand.is_dir():
            out.append(cand)
    return out


def load_env_file(path: Path) -> dict[str, str]:
    """Source a bash-style ``export FOO=bar`` file into a dict."""
    out: dict[str, str] = {}
    if not path.exists():
        return out
    for raw in path.read_text().splitlines():
        s = raw.strip()
        if not s or s.startswith("#"):
            continue
        if s.startswith("export "):
            s = s[len("export "):]
        if "=" not in s:
            continue
        k, v = s.split("=", 1)
        v = v.strip()
        if (v.startswith("'") and v.endswith("'")) or (
            v.startswith('"') and v.endswith('"')
        ):
            v = v[1:-1]
        out[k.strip()] = v
    return out


def run_verify_dir(
    source_dir: Path,
    artifact_dir: Path,
    extra_env: dict[str, str],
    log_path: Path,
    *,
    minimal: bool = False,
    extra_args: Optional[list[str]] = None,
) -> int:
    """Invoke ``python -m bmc_agent.cli verify-dir`` on ``source_dir``.

    Returns the process exit code. Output is streamed to ``log_path``.
    """
    artifact_dir.mkdir(parents=True, exist_ok=True)
    env = os.environ.copy()
    env.update(extra_env)

    cmd = [
        "uv", "run", "python", "-m", "bmc_agent.cli", "verify-dir",
        "--source-dir", str(source_dir),
        "--driver", "ossfz",
        "--output", str(artifact_dir),
        "--enable-realism-check",
        "--enable-dynamic-validation",
        # OpenRouter mixed-stack: tool-use works via Haiku 4.5.
        # Re-enable Phase 3e in-pipeline triage agent (G1-G5 audit).
        "--enable-phase-3e-triage",
        # Use v2 spec generator now that Haiku produces parseable JSON.
    ]
    # Per-project domain knowledge — hand-curated docs covering format
    # invariants, public-API entry points, historical CVE classes, and
    # known-FP suppression hints. Providing this also short-circuits
    # bmc-agent's Pass 1.5 LLM-driven domain analysis (saves time + cost).
    project_name = extra_env.pop("__BMC_AGENT_PROJECT__", "")
    if project_name:
        dk_path = Path(__file__).parent / "domain_knowledge" / f"{project_name}.md"
        if dk_path.exists():
            cmd.extend(["--domain-knowledge", str(dk_path)])
            print(f"[oss_fuzz_sweep] using domain knowledge: {dk_path}")
    # Skip test / utility files for the current project (set by caller).
    for pat in extra_env.pop("__BMC_AGENT_EXCLUDE_PATS__", "").split("|"):
        if pat:
            cmd.extend(["--exclude", pat])
    # Add include directories: just the source dir itself + any
    # *direct* subdir of the project root that holds public headers.
    # We DO NOT rglob the whole tree because OSS-Fuzz projects' contrib/,
    # tests/, and arch-specific subdirs pollute the symbol space and
    # cause spec-gen to discover non-library functions (e.g. libpng's
    # contrib/gregbook/readpng2.c was being processed via the wide
    # include scan). The per-project corpus_prep should have already
    # placed any required generated headers in the source dir.
    cmd.extend(["--include-dir", str(source_dir)])
    # One sibling of source_dir as a header location, max — for
    # libtiff (libtiff/) and expat (expat/lib/) the source_dir IS the
    # subdir; for libpng / zstd source_dir IS the project root.
    parent_root = source_dir.parent if source_dir.parent != source_dir else source_dir
    for subdir in sorted(parent_root.iterdir()) if parent_root.is_dir() else []:
        if subdir == source_dir or not subdir.is_dir():
            continue
        # Only direct subdirs whose name suggests headers/config.
        if subdir.name in {"include", "config", "build"}:
            if any(subdir.glob("*.h")):
                cmd.extend(["--include-dir", str(subdir)])
    # Explicit per-project header dirs the heuristic above can't infer.
    for inc in extra_env.pop("__BMC_AGENT_EXTRA_INCLUDES__", "").split("|"):
        if inc:
            cmd.extend(["--include-dir", inc])
    if minimal:
        cmd.append("--minimal")
    if extra_args:
        cmd.extend(extra_args)

    log_path.parent.mkdir(parents=True, exist_ok=True)
    print(f"[oss_fuzz_sweep] launching: {' '.join(cmd)}")
    print(f"[oss_fuzz_sweep] log: {log_path}")
    with log_path.open("w") as fh:
        return subprocess.call(cmd, env=env, stdout=fh, stderr=subprocess.STDOUT)


def iter_bug_reports(artifact_dir: Path) -> Iterable[tuple[Path, dict]]:
    """Walk per-CEx bug reports under ``artifact_dir``.

    bmc-agent writes TWO levels of report:

      <fn>/bug_report.json          — CBMC-stage summary, NO confidence /
                                      realism / triage. Only 6 fields.
      <fn>/bug_reports/<name>.json  — per-CEx detailed report AFTER realism
                                      + dynamic + confidence stages. THIS is
                                      what we want for the triage gate.

    The per-CEx schema (observed 2026-05-29):
      {
        "saved_at": "...",
        "report": {
          "function_name": "...",
          "driver_name": "...",
          "bug_type": "...",
          "violated_property": "...",
          "counterexample": {...},
          "call_chain": [...],
          "confidence": "confirmed_dynamic|...|likely|unlikely|null",
          "realism_check": {"verdict": "realistic|unrealistic|uncertain", "reasoning": "..."},
          "cex_outcome": "...",
          "dynamic_outcome": "...",
          "dynamic_signal": "..."
        }
      }
    """
    for path in artifact_dir.rglob("bug_reports/*.json"):
        try:
            data = json.loads(path.read_text())
        except (json.JSONDecodeError, OSError):
            continue
        payload = data.get("report") if isinstance(data, dict) else None
        if not isinstance(payload, dict):
            continue
        yield path, payload


def passes_triage(report: dict) -> bool:
    """Apply the STRICT triage gate.

    Per-CEx report (from bug_reports/<name>.json). Keep iff:
      * confidence ∈ confirmed_* tiers
      * realism_check.verdict == "realistic"
      * the in-pipeline TriageToolsAgent verdict (if it ran) is NOT
        LIKELY_FP — that gate applies the G1-G5 audit and catches the
        FPs the realism agent (Gemini Flash) sometimes mis-judges.

    Background: observed 2026-05-29 that Gemini Flash flagged 3
    tif_readproc/tif_diroff cases as "realistic" that are textbook
    G4-FP (caller-established precondition via TIFFOpen) and
    G5-structural-invariant-bound (overflow trio in TIFFFetchDirectory).
    The G1-G5 triage agent on Haiku is the precision backstop —
    require its verdict before auto-pushing.
    """
    # (C) Accuracy filter — count only genuine memory-safety violations.
    # Two inaccuracy classes observed 2026-05-30 that must never reach the
    # "confirmed bug" gate:
    #   * unwinding-assertion artifacts (e.g. cmark_chunk_rtrim.unwind.0):
    #     these mean "the loop bound was too small", NOT a bug.
    #   * semantic / spec-violation properties: high FP rate, not a crash.
    # Keep memory_safety (OOB read/write, NULL/invalid deref) — the classes
    # the ASan-replay oracle can confirm as real crashes.
    prop = (report.get("violated_property") or report.get("cbmc_property") or "").lower()
    if ".unwind." in prop or "unwinding" in prop:
        return False
    bug_type = (report.get("bug_type") or "").lower()
    if bug_type and bug_type != "memory_safety":
        return False

    confidence = report.get("confidence", "")
    if confidence not in CONFIRMED_TIERS:
        return False
    realism = report.get("realism_check") or {}
    verdict = (realism.get("verdict") or "").lower()
    if verdict != "realistic":
        return False
    # Don't auto-push if triage downgraded.
    triage = report.get("triage") or report.get("triage_result") or {}
    triage_verdict = (triage.get("verdict") or "").lower()
    if triage_verdict in ("likely_fp", "needs_human"):
        return False
    return True


def render_finding(report: dict, source_path: Path, run_id: str, meta: ProjectMeta) -> str:
    """Render a minimal but human-reviewable markdown finding from a per-CEx report."""
    fn = report.get("function_name", "<unknown>")
    # Per-CEx schema uses singular "counterexample" + "violated_property"
    cex = report.get("counterexample") or {}
    cprop = report.get("violated_property") or cex.get("property") or "<unknown>"
    conf = report.get("confidence", "<unknown>")
    realism = report.get("realism_check") or {}
    realism_verdict = realism.get("verdict", "")
    realism_reasoning = realism.get("reasoning", "")
    triage = report.get("triage", {}) or report.get("triage_result", {}) or {}
    triage_verdict = triage.get("verdict", "")
    triage_reasoning = triage.get("reasoning", "")

    return f"""# {meta.name}: {fn} — {cprop}

**Project**: {meta.name} ({meta.homepage})
**Upstream repo**: {meta.main_repo}
**Sweep run**: {run_id}
**Bug-report source**: `{source_path.name}`
**Confidence tier**: `{conf}`
**Realism verdict**: `{realism_verdict}`
**Triage verdict**: `{triage_verdict}`

## CBMC property
```
{cprop}
```

## Function under test
`{fn}`

## Realism check reasoning
{realism_reasoning or "_(not recorded)_"}

## Triage reasoning
{triage_reasoning or "_(not recorded)_"}

## Disclosure status
- ⏳ Upstream-status verification needed against shipping {meta.name} release.
- ⏳ Crafted reproducer / ASan witness pending.
- Primary OSS-Fuzz contact: {meta.primary_contact or "(see project.yaml)"}

## Raw report
See `{source_path}` in the artifact tree for the full JSON.
"""


def upload_findings(
    findings: list[tuple[Path, dict, str]],
    meta: ProjectMeta,
    run_id: str,
    embargoed_root: Path,
    dry_run: bool,
) -> Path:
    """Write per-bug markdown reports to the embargoed repo and (optionally) commit + push."""
    sweep_dir = embargoed_root / "findings" / "oss-fuzz" / meta.name / "sweeps" / run_id / "audit"
    sweep_dir.mkdir(parents=True, exist_ok=True)

    summary_lines = [
        f"# {meta.name} sweep {run_id} — summary",
        "",
        f"- Upstream: {meta.main_repo}",
        f"- Run UTC: {datetime.now(timezone.utc).isoformat(timespec='seconds')}",
        f"- Confirmed+realistic findings: **{len(findings)}**",
        "",
    ]
    if not findings:
        summary_lines.append("_No bugs passed the triage gate this run._")
    else:
        summary_lines.append("| # | Function | Property | Confidence | Realism |")
        summary_lines.append("|---|---|---|---|---|")

    for i, (src_path, report, md) in enumerate(findings, start=1):
        fn = report.get("function", "<unknown>")
        safe_fn = "".join(c if c.isalnum() or c in "_-" else "_" for c in fn)
        safe_prop = "".join(c if c.isalnum() or c in "_-" else "_" for c in report.get("cbmc_property", ""))
        out_path = sweep_dir / f"{i:02d}_{safe_fn}_{safe_prop[:40]}.md"
        out_path.write_text(md)
        summary_lines.append(
            f"| {i} | `{fn}` | `{report.get('cbmc_property','')}` | "
            f"`{report.get('confidence','')}` | "
            f"`{(report.get('realism_check') or {}).get('verdict','')}` |"
        )

    summary_path = sweep_dir.parent / "SWEEP_SUMMARY.md"
    summary_path.write_text("\n".join(summary_lines) + "\n")

    if dry_run:
        print(f"[oss_fuzz_sweep] DRY RUN — would have committed {len(findings)} finding(s) and the SWEEP_SUMMARY.md")
        return sweep_dir

    _run(["git", "add", "-A"], cwd=embargoed_root)
    status = _run(["git", "status", "--short"], cwd=embargoed_root, capture=True)
    if not status.stdout.strip():
        print("[oss_fuzz_sweep] no changes to commit")
        return sweep_dir
    commit_msg = (
        f"oss-fuzz/{meta.name}: sweep {run_id} — {len(findings)} confirmed+realistic finding(s)\n\n"
        f"Auto-uploaded by tools/oss_fuzz_sweep.py.\n"
        f"Triage gate: confidence ∈ confirmed_* AND realism=realistic.\n"
        f"Upstream-status verification pending for each finding.\n"
    )
    _run(["git", "commit", "-m", commit_msg], cwd=embargoed_root)
    try:
        _run(["git", "push"], cwd=embargoed_root)
    except subprocess.CalledProcessError as exc:
        print(f"[oss_fuzz_sweep] WARNING: push failed: {exc}; commit is local")
    return sweep_dir


def sweep_one_project(args: argparse.Namespace) -> int:
    meta = ProjectMeta.fetch(args.project)
    print(f"[oss_fuzz_sweep] project={meta.name} repo={meta.main_repo} lang={meta.language}")

    project_root = clone_or_pull(meta, args.corpus_root)
    prepare_corpus(meta.name, project_root)
    source_dir = discover_sources(project_root)
    print(f"[oss_fuzz_sweep] source-dir: {source_dir}")

    run_id = datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")
    artifact_dir = args.artifact_root / meta.name / run_id
    log_path = args.log_root / f"{meta.name}_{run_id}.log"

    # Bound /tmp growth under 24/7 operation: keep only the 2 newest prior
    # run dirs for this project before starting a new one. Findings are
    # already uploaded to the embargoed repo, so old artifact trees are
    # disposable. Runs as a fresh process each project, so this self-prune
    # takes effect without restarting the continuous loop.
    proj_artifacts = args.artifact_root / meta.name
    if proj_artifacts.is_dir():
        prior = sorted((d for d in proj_artifacts.iterdir() if d.is_dir()),
                       key=lambda d: d.name)
        for stale in prior[:-2]:
            import shutil
            shutil.rmtree(stale, ignore_errors=True)
            print(f"[oss_fuzz_sweep] pruned old artifacts: {stale}")

    extra_env = load_env_file(args.env_file)
    extra_env.setdefault("BMC_AGENT_RUN_NOTE", f"run_id={run_id}")
    # Smuggle per-project exclude globs through extra_env so the
    # run_verify_dir() helper can stitch them into --exclude flags.
    excl = excludes_for(meta.name)
    if excl:
        extra_env["__BMC_AGENT_EXCLUDE_PATS__"] = "|".join(excl)
    # Smuggle project name for domain-knowledge lookup.
    extra_env["__BMC_AGENT_PROJECT__"] = meta.name
    # Smuggle explicit header dirs for projects whose public headers live
    # in a nested layout (brotli: c/include/brotli/*.h).
    extra_inc = extra_includes_for(meta.name, project_root)
    if extra_inc:
        extra_env["__BMC_AGENT_EXTRA_INCLUDES__"] = "|".join(str(p) for p in extra_inc)

    if args.corpus_only:
        print("[oss_fuzz_sweep] --corpus-only: skipping verify-dir")
        return 0

    rc = run_verify_dir(
        source_dir, artifact_dir, extra_env, log_path,
        minimal=args.minimal,
        extra_args=args.extra,
    )
    print(f"[oss_fuzz_sweep] verify-dir exit={rc}")

    findings = []
    for src_path, report in iter_bug_reports(artifact_dir):
        if not passes_triage(report):
            continue
        md = render_finding(report, src_path, run_id, meta)
        findings.append((src_path, report, md))
    print(f"[oss_fuzz_sweep] {len(findings)} bug(s) passed triage gate")

    sweep_dir = upload_findings(
        findings, meta, run_id, args.embargoed_root, args.dry_run,
    )
    print(f"[oss_fuzz_sweep] report dir: {sweep_dir}")
    return rc


def build_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(description=__doc__.split("\n")[0])
    p.add_argument("--project", required=True, help="OSS-Fuzz project name (e.g. libpng)")
    p.add_argument("--corpus-root", type=Path, default=DEFAULT_CORPUS_ROOT)
    p.add_argument("--artifact-root", type=Path, default=DEFAULT_ARTIFACT_ROOT)
    p.add_argument("--embargoed-root", type=Path, default=DEFAULT_EMBARGOED_ROOT)
    p.add_argument("--env-file", type=Path, default=DEFAULT_ENV_FILE,
                   help="Optional environment file with LLM routing settings")
    p.add_argument("--log-root", type=Path, default=DEFAULT_LOG_ROOT,
                   help="Directory for per-project sweep logs")
    p.add_argument("--dry-run", action="store_true",
                   help="Render reports but don't commit/push to embargoed repo")
    p.add_argument("--corpus-only", action="store_true",
                   help="Clone/sync the project source then stop (smoke test)")
    p.add_argument("--minimal", action="store_true",
                   help="Pass --minimal to verify-dir (disable optional AI layers)")
    p.add_argument("--extra", nargs=argparse.REMAINDER,
                   help="Extra args forwarded to verify-dir verbatim")
    return p


def main() -> int:
    args = build_parser().parse_args()
    return sweep_one_project(args)


if __name__ == "__main__":
    sys.exit(main())
